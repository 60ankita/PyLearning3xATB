# Ternary Operator
ankita_marks = 90
amit_marks = 97

#   x > y -> do something - print("pramod")
# y > x -> do something else -> print("amit)
# print("pramod is winner" if pramod_marks > amit_marks else "amit is winner")
print("Ankita wins" if ankita_marks < amit_marks else "Amit wins")

if ankita_marks > amit_marks:
    print("Ankita is the winner")
else:
    print("Amit is the winner")

